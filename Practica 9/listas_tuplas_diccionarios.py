# Listas
lista = [1, 2, 3, 4]
print("Lista:", lista)
lista.append(5)
print("Lista modificada:", lista)

# Tuplas
tupla = (10, 20, 30)
print("Tupla:", tupla)

# Diccionarios
dic = {"nombre": "Diego", "edad": 20}
print("Diccionario:", dic)
print("Nombre:", dic["nombre"])
dic["edad"] = 21
print("Diccionario modificado:", dic)
