# Mientras (while)
contador = 0
while contador < 5:
    print("Contador:", contador)
    contador += 1

# For en lista
frutas = ["manzana", "pera", "uva"]
for fruta in frutas:
    print("Fruta:", fruta)

# For en diccionario
persona = {"nombre": "Ana", "edad": 25}
for llave, valor in persona.items():
    print(llave, ":", valor)
